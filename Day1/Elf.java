public class Elf {
    private int caloriesCarried;
    private int elfNumber;

    public Elf(int caloriesCarried, int elfNumber) {
        this.caloriesCarried = caloriesCarried;
        this.elfNumber = elfNumber;
    }

    public int getCaloriesCarried() {
        return caloriesCarried;
    }

    public int getElfNumber() {
        return elfNumber;
    }
}
